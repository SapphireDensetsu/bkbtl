extern const BYTE UkncFont[];
extern const int UkncFontSize;

SDL_Surface* m_FontSurfaceUknc;


void Fonts_Initialize()
{
    m_FontSurfaceUknc = SDL_CreateRGBSurface(0, 8, 224 * 11, 8, 0,0,0,0);
    SDL_SetColorKey(m_FontSurfaceUknc, SDL_SRCCOLORKEY, 0);
    //SDL_SetAlpha(m_FontSurfaceUknc, SDL_SRCALPHA, 128);

    SDL_Color colors[2];
    colors[0].r = colors[0].g = colors[0].b = 0;
    colors[1].r = colors[1].g = colors[1].b = 255;
    SDL_SetPalette(m_FontSurfaceUknc, SDL_LOGPAL|SDL_PHYSPAL, colors, 0, 2);

    // Prepare font surface for UkncFont
    SDL_LockSurface(m_FontSurfaceUknc);
    const BYTE* pSrc = UkncFont;
    BYTE* pDst = (BYTE*)m_FontSurfaceUknc->pixels;
    for (int i = 0; i < 224; i++)
    {
        for (int j = 0; j < 11; j++)
        {
            BYTE cc = *pSrc++;
            for (int b = 0; b < 8; b++)
            {
                *pDst++ = (cc & 1) ? 1 : 0;
                cc = cc >> 1;
            }
        }
    }
    SDL_UnlockSurface(m_FontSurfaceUknc);
}

void Fonts_Finalize()
{
    SDL_FreeSurface(m_FontSurfaceUknc);
    m_FontSurfaceUknc = NULL;
}


void Font_DrawText(SDL_Surface* fontSurface, int x, int y, int charw, int charh, const char *str)
{
    int charline;
    char ch;
    SDL_Rect src, dest;

    if (str == NULL) return;
    if (y >= SCREEN_HEIGHT) return;

    while (*str != 0)
    {
        ch = *str;
        if (ch >= ' ')
        {
            charline = (int)ch - ' ';

            src.x = 0;  src.y = charline * charh;
            src.w = charw;  src.h = charh;
            dest.x = x;  dest.y = y;
            dest.w = charw;  dest.h = charh;
            SDL_BlitSurface(fontSurface, &src, SDL_GetVideoSurface(), &dest);
        }
        x += charw;
        if (x >= SCREEN_WIDTH) break;

        str++;
    }
}
void Font_DrawText(int x, int y, const char *str)
{
    Font_DrawText(m_FontSurfaceUknc, x, y, 8, 11, str);
}


